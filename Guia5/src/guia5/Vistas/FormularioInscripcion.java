    private void cargarCombobox(){
        AlumnoData alumnodata = new AlumnoData();
        ArrayList <Alumno> lista = new ArrayList<>();
        lista.addAll(alumnodata.listarAlumno());

        for (Alumno a : lista){
            jCBAlumno.addItem(a);
        }
    }
    private void borrarFilas(){
        int f=jTInsc.getRowCount()-1;      
        for(;f>=0;f--){
            modelo.removeRow(f);
        }
    }
