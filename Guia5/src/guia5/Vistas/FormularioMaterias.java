    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        try{
            if(jTCodigo.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Complete el campo");
                return;
            }
            Materia mat;
            int cod = Integer.parseInt(jTCodigo.getText());
            if(cod<0){
                jTCodigo.setText(null);
                JOptionPane.showMessageDialog(this, "Debe ingresar un cÃ³digo vÃ¡lido");
            }else{ 
            mat =matdat.buscarMateria(cod);           
        
            if(mat==null){
                borrarCampos();
            }else{
                jTNombre.setText(mat.getNombre());
                jTAnio.setText(mat.getAnioMateria()+"");
                jREstado.setSelected(true);
      
                    
                    }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Ingrese nÃºmeros en el campo cÃ³digo");
        }
        
    
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBNuevoActionPerformed
       try{
           if(jTAnio.getText().isEmpty()  || jTNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
            return;
            }
            Materia mat=new Materia();
            int anio = Integer.parseInt(jTAnio.getText());            
            mat.setNombre(jTNombre.getText());
            mat.setAnioMateria(anio);
            mat.setActivo(true);         
            matdat.guardarMateria(mat);
            borrarCampos();
        }catch(Exception ex){
           JOptionPane.showMessageDialog(this, "Ingrese en los campos cÃ³didgo y/o aÃ±o");
        }
    }//GEN-LAST:event_jBNuevoActionPerformed
    
    
    private void jGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGuardarActionPerformed
       if(jTCodigo.getText().isEmpty()||jTNombre.getText().isEmpty()|| jTAnio.getText().isEmpty()){
        JOptionPane.showMessageDialog(null,"Debe completar todos los campos");
        return;
       }
      
        Materia mat=new Materia();                       
        int codigo = Integer.parseInt(jTCodigo.getText());
        int anio = Integer.parseInt(jTAnio.getText());
        mat.setIdMateria(codigo);
        mat.setNombre(jTNombre.getText());
        mat.setAnioMateria(anio);        
        matdat.modificarMateria(mat);
        borrarCampos();
        
        
    }//GEN-LAST:event_jGuardarActionPerformed

    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
      if(jTCodigo.getText().isEmpty()){
      JOptionPane.showMessageDialog(this,"Debe ingresar un codigo");
      return;
      }
        int cod  = Integer.parseInt(jTCodigo.getText());
       matdat.eliminarMateria(cod);
       borrarCampos();
       
    }//GEN-LAST:event_jBEliminarActionPerformed

